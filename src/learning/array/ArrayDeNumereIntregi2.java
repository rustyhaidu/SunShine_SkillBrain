package learning.array;

public class ArrayDeNumereIntregi2 {
    public static void main(String[] args) {
        int[] arr = {2, 3, 4, 5, 6};
        arr[0] = 1;
        arr[1] = 10;
        arr[2] = 11;

        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);
        }
    }
}
