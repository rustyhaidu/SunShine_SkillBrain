package learning.array;

public class ArrayDeStringuri {
    public static void main(String[] args) {
        String[] arr = new String[4];
        arr[0] = "Mere";
        arr[1] = "Pere";
        arr[2] = "Caise";

        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);
        }
    }
}
