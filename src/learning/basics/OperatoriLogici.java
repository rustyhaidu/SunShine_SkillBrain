package learning.basics;

public class OperatoriLogici {
    public static void main(String[] args) {
        int a = 9;
        int b = 10;
        if (a > b) {
            System.out.println("Da, 9 este mai mare decat 7");
        } else {
            System.out.println(b + " este mai mare decat "+ a);
        }
    }
}
